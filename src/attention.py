class TransformerBlock2(nn.Module):
    # 用像素引导semantic恢复
    def __init__(self, x_dim, y_dim, input_resolution, num_heads = 8, ffn_expansion_factor = 2, bias = False):
        super(TransformerBlock2, self).__init__()

        self.aff = nn.Sequential(
            nn.Conv2d(x_dim, x_dim, 4, 2, 1),
            nn.LeakyReLU(negative_slope=0.1, inplace=False),
            nn.Conv2d(x_dim, x_dim, 4, 2, 1),
            nn.LeakyReLU(negative_slope=0.1, inplace=False)
        )
        self.attn = Attention(y_dim, x_dim, num_heads, bias)

        # self.aff = SPADE(main_dim = x_dim, sty_dim = y_dim)
        # self.conv1 = nn.Conv2d(x_dim + y_dim, y_dim, kernel_size=3, padding = 1)
